empty :: (MonadAppHost t m) => HtmlT m ()
empty = return ()  

elWith :: MonadAppHost t m => ElementConfig t m -> Attributes t m -> HtmlT m a -> HtmlT m a
elWith config attrs = el $ config & attributes <>~ attrs 

elWith_ :: MonadAppHost t m => ElementConfig t m -> Attributes t m -> HtmlT m ()
elWith_ config attrs = el_ $ config & attributes <>~ attrs 
  
elWith' :: MonadAppHost t m => ElementConfig t m -> Attributes t m -> HtmlT m a -> HtmlT m (Element t, a)
elWith' config attrs = el' $ config & attributes <>~ attrs 



